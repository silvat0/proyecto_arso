package arso.restaurantes.modelo;


public class ResumenValoracion {
	

	private String idOpinion;
	private float calificacionMedia;
	private int NumValoraciones;
	
	
	
	public String getIdOpinion() {
		return idOpinion;
	}
	
	public void setIdOpinion(String idOpinion) {
		this.idOpinion = idOpinion;
	}
	
	public float getCalificacionMedia() {
		return calificacionMedia;
	}
	
	public void setCalificacionMedia(float calificacionMedia) {
		this.calificacionMedia = calificacionMedia;
	}
	
	public int getNumValoraciones() {
		return NumValoraciones;
	}
	
	public void setNumValoraciones(int numValoraciones) {
		NumValoraciones = numValoraciones;
	}
	
}
